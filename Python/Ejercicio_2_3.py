print("\n\tMayor o Menor\n")
X = int(input("\nIngrese el primer numero: "))
Y = int(input("\nIngrese el Segundo numero: "))
if X > Y:
    print("\n\tEs Mayor el primer numero :",X)
    print("\n\tEs Menor el sergundo numero :",Y)
elif X == Y:
    print("\n\tEl numero",X,"y",Y,"son iguales")
else:
    print("\n\tEs Menor el primer numero :",X)
    print("\n\tEs Mayor el sergundo numero :",Y)